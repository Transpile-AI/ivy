# global
import ivy


def acos(x, name="acos"):
    return ivy.acos(x)


def acosh(x, name="acosh"):
    return ivy.acosh(x)


def concat(values, axis, name="concat"):
    return ivy.concat(values, axis)


def cos(x, name="acosh"):
    return ivy.cos(x)


def fill(dims, value, name="full"):
    return ivy.full(dims, value)
